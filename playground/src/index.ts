/**
 * Adds two numbers together.
 *
 * @example
 *
 * ```js
 * add(1, 2); // 3
 * ```
 *
 * @group utils
 */
export function add(a: number, b: number) {
  return a + b;
}

/**
 * Adds two numbers together.
 *
 * @example
 *
 * ```js
 * subtract(2, 1); // 1
 * ```
 *
 * @group utils
 */
export function subtract(a: number, b: number) {
  return a - b;
}
